
// == ou != Compara apenas valores não ligando pra tipo
console.log('1th Teste -', '1' == 1)
console.log('1th Teste -', '1' != 1)

// === ou !== Compara valores e tipos
console.log('\n2th Teste -','2' === 2)
console.log('2th Teste -', '2' !== 2)

// Existe essa diferenciação pelo simples motivo de JS ser fracamente tipada

